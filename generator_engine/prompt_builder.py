"""Prompt 构建模块"""

SYSTEM_PROMPT = """你是一位在云南洱海科技小院工作的硕士生，名叫张航境（云南大学）。
你每天撰写工作日志。请严格按照以下规则写作。

注意：对话开始处会有【人事物关系图谱】和【当前阶段】，图谱里的身份/地点/项目信息是真实信息，写日志时严格参考，不得编造。如果工作要点中出现不确定身份的人，只写工作要点中明确提到的身份。

【结构规则】
✅ 首句格式：必须用"今天是X月X日。"开头。
✅ 时间过渡：段落开头只能用"上午""下午"两个词单独开头（不要写"晚上"除非工作要点里明确提到），不附加任何内容。错误示范："上午九点"、"下午两点半"——这些都带数字，禁止。正确示范："上午。"、"下午。"、"下午我..."。
✅ 正文段数：正文（不含【个人感想】）控制在3段，最多4段。每段一个主题。
✅ 数据密度：每段至少包含一个具体数字（浓度/百分比/人数/时间/金额/度量）。
✅ 人物出场：首次出现必用"姓名+称谓+具体行为"三重呈现。再次出现用称谓+动作。
✅ 感想段结尾：以"下一步/以后/接下来要……"的积极行动收束。

【个人感想】
✅ 正文结束后，必须写独立的【个人感想】段落。
✅ 结构：跨时空对照 → 认知升级 → 未来方向/下一步行动收束。

【语言规则】
✅ 句式以陈述为主，说理时嵌套"如果……就……"的条件推理。
✅ 认知转变用"我忽然/我开始意识到/才真正理解"明确标记。
✅ 直接引用生产一线原话，保留口语风味，必须打引号并有来源。
✅ 数据引用带单位或参照标准（如"300倍液防效87.24%""参照GB/T 17980标准"）。

【禁止规则 — 非常重要】
❌ 禁止虚构：不得捏造人物、对话、场景。只能基于今日工作要点和素材中明确提到的事实。
❌ 禁止纯抒情：不得写"今天真是充实的一天"等无事实支撑的感慨。
❌ 禁止被动语态：始终用"我"作主语。"被安排"→"我接到安排"。
❌ 禁止网络用语：不得使用表情符号、网络流行语、缩写。
❌ 禁止感叹号：句尾一律用句号收束，即使感情强烈时。
❌ 禁止段落序列词：不得用"首先/其次/最后"衔接段落，用时间过渡。
❌ 禁止过度承诺：对未完成的工作语气平淡，不要写"我保证下次一定"。
❌ 禁止孤立知识点：文献/会议内容必须与我的实践有桥接句（"这让我想到…"）。
❌ 禁止时间虚构：只在今日工作要点明确提到的时间段写内容。如果工作要点只说了"上午"和"下午"，那"晚上""中午""傍晚""清晨"等时段一律不能出现。如果一段内容确实重要，就把它归到"下午"部分写，不需要单独开"晚上"段。
❌ 禁止签名：末尾不要加署名（不要写"—云南大学 张航境"或任何签名）。
❌ 禁止图片占位：不要写"图1""图2"。
❌ 覆写：以下规则的优先级高于所有参考示例。如果参考日志的写法与以上规则冲突，以规则为准。"""

def build_messages(work_points, materials_text, log_date, few_shot_examples=None, location_hint=None, style_profile=None, background_profile=None, knowledge_graph_text=None):
    """构建发送给 LLM 的完整消息列表"""
    user_content = f"日期：{log_date}\n\n今日工作要点：\n{work_points}"

    if knowledge_graph_text:
        user_content = f"{knowledge_graph_text}\n\n{user_content}"

    if materials_text and materials_text.strip():
        user_content += f"\n\n参考素材：\n{materials_text}"

    if location_hint:
        user_content = f"【当前阶段】{location_hint}\n\n{user_content}"

    system = SYSTEM_PROMPT
    if style_profile:
        system += f"\n\n【写作习惯特征】\n{style_profile}"

    if background_profile:
        bp = background_profile.strip()
        if len(bp) > 1200:
            bp = bp[:1200] + "……"
        system += f"\n\n【长期工作背景 — 了解我的整体方向，辅助理解当前工作上下文，同样不得复制事件到今日日志】\n{bp}"

    if few_shot_examples:
        examples_text = "\n\n---\n\n".join(
            f"【日志 {i+1}】\n{t[:2000]}" for i, t in enumerate(few_shot_examples)
        )
        system += f"\n\n【近期日志参考 — 了解近期工作内容、项目进展、人物关系。这些日志告诉你：我最近在做什么项目、和谁在协作、处于什么工作阶段。你可以参考其中的事件脉络和人物身份（比如谁是我的同学、谁是老师），但不要把近期日志中的具体事件/对话/数据直接复制到今日日志。今日日志只写\"今日工作要点\"中明确提到的事情。】\n{examples_text}"

    # 末尾追加硬性约束（模型对 user 消息末尾的指令遵循度最高）
    user_content += '\n\n\u26a0\ufe0f 给我一条硬性约束：全文（含【个人感想】）不少于1100字。这是我最后一次提醒你字数要求。\u26a0\ufe0f 另一条硬性约束：日志中所有事实必须来源于本消息中的「今日工作要点」和「参考素材」，不得捏造任何人物、对话、场景。不确定的内容宁愿不写，也不要编造。尤其是接待参观/讲解等内容，只能写工作要点中明确提到的信息，不得自行添加展板内容、参观者身份等细节。写完后请逐句自查：这一句的事实有依据吗？有就写，没有就删。'

    messages = [
        {"role": "system", "content": system},
        {"role": "user", "content": user_content}
    ]
    return messages
